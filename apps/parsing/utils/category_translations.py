def translate_category(category_name):
    """Переводит категории Avito в понятные названия"""
    if not category_name:
        return 'Не указана'

    # Приводим к нижнему регистру для сравнения
    category_lower = category_name.lower()

    # Основные категории из навигации Avito
    category_mapping = {
        # Электроника
        'электроника': 'Электроника',
        'телефоны': 'Телефоны',
        'мобильные телефоны': 'Мобильные телефоны',
        'смартфоны': 'Смартфоны',
        'планшеты': 'Планшеты',
        'ноутбуки': 'Ноутбуки',
        'компьютеры': 'Компьютеры',
        'фототехника': 'Фототехника',
        'аудиотехника': 'Аудиотехника',

        # Одежда и обувь
        'одежда': 'Одежда',
        'обувь': 'Обувь',
        'женская одежда': 'Женская одежда',
        'мужская одежда': 'Мужская одежда',
        'детская одежда': 'Детская одежда',

        # Дом и сад
        'дом и сад': 'Дом и сад',
        'мебель': 'Мебель',
        'бытовая техника': 'Бытовая техника',
        'посуда': 'Посуда',

        # Транспорт
        'транспорт': 'Транспорт',
        'автомобили': 'Автомобили',
        'мотоциклы': 'Мотоциклы',
        'велосипеды': 'Велосипеды',

        # Недвижимость
        'недвижимость': 'Недвижимость',
        'квартиры': 'Квартиры',
        'дома': 'Дома',
        'комнаты': 'Комнаты',

        # Хобби и отдых
        'хобби и отдых': 'Хобби и отдых',
        'спорт': 'Спорт',
        'книги': 'Книги',
        'музыка': 'Музыка',

        # Животные
        'животные': 'Животные',
        'собаки': 'Собаки',
        'кошки': 'Кошки',

        # Услуги
        'услуги': 'Услуги',
        'ремонт': 'Ремонт',
        'красота': 'Красота',
    }

    # Прямое соответствие
    if category_lower in category_mapping:
        return category_mapping[category_lower]

    # Поиск по частичному совпадению
    for key, value in category_mapping.items():
        if key in category_lower:
            return value

    # Если не нашли, возвращаем оригинальное название с капитализацией
    return category_name.capitalize()